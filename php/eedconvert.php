function sdk_conv_ansiUtf8($valor) 
{

    $ansi_Utf8 = array(
    
    "" => "\u00e0",
    "" => "\u00e2",
    "" => "\u00e4",
    "" => "\u00e7",
    "" => "\u00e8",
    "" => "\u00e9",
    "" => "\u00ea",
    "" => "\u00eb",
    "" => "\u00ee",
    "" => "\u00ef",
    "" => "\u00f4",
    "" => "\u00f6",
    "" => "\u00f9",
    "" => "\u00fb",
    "" => "\u00fc",
    "" => "\u0080" );

    return strtr($valor, $ansi_Utf8); 
}

